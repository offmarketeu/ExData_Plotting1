setwd("E:/COURSERA/Data Science/Course4- Data exploratory/Week_1/Assigment1/")
file<-"household_power_consumption.txt"



# Read original table and select dataframe to analyst


data<- read.table(file,header = TRUE,sep = ";",colClasses = c("character", "character", rep("numeric",7)),na = "?")
setplot3 <- data[data$Date %in% c("1/2/2007","2/2/2007") ,]



# Preparing data

datetime <- strptime(paste(setplot3$Date, setplot3$Time, sep=" "), "%d/%m/%Y %H:%M:%S") 
globalActivePower <- as.numeric(setplot3$Global_active_power)
subMetering1 <- as.numeric(setplot3$Sub_metering_1)
subMetering2 <- as.numeric(setplot3$Sub_metering_2)
subMetering3 <- as.numeric(setplot3$Sub_metering_3)

# Plot 3

png("plot3.png", width = 480, height = 480, units = "px", bg = "white")
plot(datetime, subMetering1, type="l", ylab="Energy Submetering", xlab="")
lines(datetime, subMetering2, type="l", col="red")
lines(datetime, subMetering3, type="l", col="blue")
legend("topright", c("Sub_metering_1", "Sub_metering_2", "Sub_metering_3"), lty=1, lwd=2.5, col=c("black", "red", "blue"))
dev.off()